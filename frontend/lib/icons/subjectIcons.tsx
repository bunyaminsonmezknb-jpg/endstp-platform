export function getSubjectIcon(subjectCode: string): SubjectIconConfig {
  return SUBJECT_ICONS[subjectCode] || DEFAULT_ICON;
}

export function getSubjectGradient(subjectCode: string): string {
  const config = getSubjectIcon(subjectCode);
  return `from-${config.color.from} to-${config.color.to}`;
}

export function isAdvancedSubject(subjectCode: string): boolean {
  return subjectCode.includes('advanced') || subjectCode.includes('_ileri');
}